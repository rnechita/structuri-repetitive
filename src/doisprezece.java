public class doisprezece {
    public static void main(String[] args) {
        int n = 12;
        switch(n){
            case 3:
            case 4:
            case 5:
                System.out.println("primavara");
                break;
            case 6:
            case 7:
            case 8:
                System.out.println("vara");
                break;
            case 9:
            case 10:
            case 11:
                System.out.println("toamna");
                break;
            case 12:
            case 1:
            case 2:
                System.out.println("iarna");
                break;
            default:
                System.out.println("luna invalida");
        }

    }
}
